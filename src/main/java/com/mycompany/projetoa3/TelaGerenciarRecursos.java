    private void excluirRecurso(){
        int selectedRow = tblRecursos.getSelectedRow();
        
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um recurso para excluir!");
            return;
        }
        int idRecurso = Integer.parseInt(tblRecursos.getValueAt(selectedRow, 0).toString());
        
        Connection conexao = Conexao.conectar();
        
        try {
            String sql = "DELETE FROM recursos WHERE id = ?";
            PreparedStatement stmt = conexao.prepareStatement(sql);
            stmt.setInt(1, idRecurso);
            stmt.executeUpdate();

            JOptionPane.showMessageDialog(this, "Recurso excluído com sucesso!");
            carregarRecursosAdmin();

            stmt.close();
            conexao.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erro ao excluir: " + e.getMessage());
        }
    }
    private void carregarRecursosAdmin(){
        Connection conexao = Conexao.conectar();
        
        try{
            String sql=
                    "SELECT r.id, u.nome AS usuario, r.titulo, r.autor, r.tipo, r.categoria " + 
                    "FROM recursos r " + 
                    "JOIN usuarios u ON r.id_usuario = u.id " + 
                    "ORDER BY r.id DESC";
            
            PreparedStatement stmt = conexao.prepareStatement(sql);
            
            ResultSet rs = stmt.executeQuery();
            
            DefaultTableModel modelo = (DefaultTableModel) tblRecursos.getModel();
            modelo.setRowCount(0);
            
            while(rs.next()){
                modelo.addRow(new Object[]{
                   rs.getInt("id"),
                   rs.getString("usuario"),
                   rs.getString("titulo"),
                   rs.getString("autor"),
                   rs.getString("tipo"),
                   rs.getString("categoria")
                });
            }
        rs.close();
        stmt.close();
        conexao.close();
        }catch(SQLException e){
            JOptionPane.showMessageDialog(this,"Erro ao carregar lista: " + e.getLocalizedMessage());
        }
    
    }
