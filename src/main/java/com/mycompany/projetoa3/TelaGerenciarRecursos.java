    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        String titulo = txtTitulo.getText();
        String autor = txtAutor.getText();
        String categoria = cmbCategoria.getSelectedItem().toString();
        String tipo = cmbTipo.getSelectedItem().toString();

        if (titulo.isEmpty() || autor.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha todos os campos!");
            return;
        }

        Connection conexao = Conexao.conectar();

        try{
            

            String sql = "INSERT INTO recursos (titulo, autor, categoria, tipo, id_usuario) VALUES (?, ?, ?, ?, ?)";
            PreparedStatement stmt = conexao.prepareStatement(sql);
            stmt.setString(1, titulo);
            stmt.setString(2, autor);
            stmt.setString(3, categoria);
            stmt.setString(4, tipo);
            stmt.setInt(5, Sessao.idUsuarioLogado);
            stmt.executeUpdate();

            JOptionPane.showMessageDialog(this, "Recurso cadastrado com sucesso!");
            carregarRecursosAdmin();

            txtTitulo.setText("");
            txtAutor.setText("");

            
            stmt.close();
            conexao.close();
        }catch (SQLException e){
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar recurso: " + e.getMessage());
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

